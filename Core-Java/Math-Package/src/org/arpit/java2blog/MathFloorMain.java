package org.arpit.java2blog;

public class MathFloorMain {

	public static void main(String[] args) {

		// Use math.floor to get largest integer lesser
		// than number
		System.out.println("floor value for 2.6 = " + Math.floor(2.6));
		System.out.println("floor value for 26.1 = " + Math.floor(26.1));
		System.out.println("floor value for -0.8 = " + Math.floor(-0.8));
		System.out.println("floor value for -6.2 = " + Math.floor(-6.2));

		float f=4.6f;
		System.out.println("floor value for 4.6f = " + Math.floor(f));	

	}
}